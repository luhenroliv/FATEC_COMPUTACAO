#Desenvolver um fluxograma e um programa em Python que leia a base e a altura de um triângulo e exiba sua área. AREA=BASE*ALTURA.

base = float(input("Digite a base do triângulo: "))
altura = float(input("Digite a altura do triângulo: "))
area = (base * altura) / 2
print("A área do triângulo é:", area)